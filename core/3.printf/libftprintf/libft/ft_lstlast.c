#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int	i;

	if (!lst)
		return (NULL);
	i = 0;
	while (lst->next)
	{
		i++;
		lst = lst->next;
	}
	return (lst);
}
